class Solution {
  public:
    string armstrongNumber(int n){
        int s=0,x=n,m=0;
        while(x!=0)
        {
            m=x%10;
            s+=(m*m*m);
            x/=10;
        }
        return s==n?"Yes":"No";
    }
};

